<template>
    <div>
        <button
            :class="{
                'bg-white text-black border-[3px] flex items-center justify-center w-8 h-8 rounded-full z-40 fixed right-[185px] top-5':
                    isSelf,
                'text-white': !isSelf,
                'text-red-500': muted && isSelf,
                hidden: !isSpeaking && !isSelf,
                'border-red-500': isSpeaking && isSelf && !muted,
                'border-white': !isSpeaking && isSelf,
            }"
            @mousedown="unmute"
            @mouseup="mute"
            @touchstart="unmute"
            @touchcancel="mute"
            @touchmove="mute"
            @touchend="mute"
            v-if="connected"
        >
            <MicrophoneIcon class="w-4" />

            <TransitionFade>
                <span
                    class="top-1/2 -translate-y-1/2 left-1/2 -translate-x-1/2 w-6 h-[2px] bg-red-500 absolute rotate-45"
                    v-if="muted && isSelf"
                ></span>
            </TransitionFade>
        </button>
    </div>
</template>

<script setup lang="ts">
import { MicrophoneIcon } from "heroicons-vue3/solid";
import { useBus } from "../composables/useBus";
import { onMounted, onUnmounted, ref, toRefs } from "vue";
import { TransitionFade } from "@morev/vue-transitions";
import DecibelMeter from "decibel-meter";
const bus = useBus();

const props = defineProps({
    isSelf: Boolean,
    userId: {
        type: Number,
        required: true,
    },
});
const { userId, isSelf } = toRefs(props);

const connected = ref(false);
const muted = ref(true);
const audioTrack = ref<MediaStreamTrack>();

const muteTimer = ref();

const mute = () => {
    muted.value = true;
    audioTrack.value!.enabled = false;
};

const unmute = () => {
    muted.value = false;
    audioTrack.value!.enabled = true;
};

const toggleMute = (e: KeyboardEvent) => {
    if (!(e.key === "V" || e.key === "v") || e.repeat) return;

    if (muted.value) {
        unmute();
    } else {
        mute();
    }
};

const isSpeaking = ref(false);

bus.on("speaking", (uid) => {
    if (uid === userId.value) {
        isSpeaking.value = true;
    }
});

bus.on("quiet", (uid) => {
    if (uid === userId.value) {
        isSpeaking.value = false;
    }
});

bus.on("vc_connected", (myAudioTrack) => {
    connected.value = true;
    audioTrack.value = myAudioTrack as MediaStreamTrack;
});

const timeout = ref<NodeJS.Timeout>();
onMounted(() => {
    if (!isSelf.value) return;

    window.addEventListener("keydown", toggleMute);
    window.addEventListener("keyup", toggleMute);

    new DecibelMeter().listenTo(0, (dB, percent, value) => {
        if (percent > 70 && !muted.value) {
            isSpeaking.value = true;
            if (timeout.value) {
                clearTimeout(timeout.value);
            }
            timeout.value = setTimeout(() => {
                isSpeaking.value = false;
            }, 300);
        }
    });
});

onUnmounted(() => {
    try {
        window.removeEventListener("keydown", toggleMute);
        window.removeEventListener("keyup", toggleMute);
    } catch (error) {}
});
</script>
